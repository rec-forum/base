Hi! 大家好! 我是Royal 的 Ex-president Dora!<br /><br />今晚要來討論一下，到底ROYAL是如何讓大家的英語進步的!<br /><br />不管是週四還是周六的聚會，都會有兩個段落，每一個段落都會有一篇文章!<br /><br />我都會先建議會員們，請先在家裡把文章讀過至少一遍! <br /><br />單字不會---&gt;沒關係，可以到ROYAL來問大家<br />文章太難---&gt;沒關係，可以來ROYAL討論<br />腦袋沒詞---&gt;沒關係，來參加個1次、2次...之後你的腦袋就會不知不覺地累積英文啦!<br /><br />其實，每週的主持人不同，主持人的大腦裡、生活環境、工作、背景...都不同，以至於ROYAL的文章呈現五花八門的色彩!<br /><br />所以，會員們就會常常面臨自己所熟悉領域的單字與&quot;不熟悉領域&quot;的單字!<br /><br />其實，我覺得遇到&quot;不熟悉領域&quot;的單字比較好，因為這可以瞬間幫你了解自己不熟悉的英文領域是哪一塊!<br /><br />像是Dora，政治、醫藥、法律...這些區域的單字就是生澀到了不行，所以每次看到這些文章就頭痛了起來~<br /><br />不過，就在讀了幾次類似領域的文章後，逐漸認識了些單字，慢慢地閱讀的時間縮短、速度也加快了。<br /><br />當你的速度加快時，相對地，自信心也開始建立了---&gt;而後開始覺得學英文就是件有趣的事情! 形成了個正向循環!<br /><br />當這樣的正向循環建立了，恭喜你，你就步入了英文漸進式進步之路!<p>Statistics: Posted by <a href="http://www.royalclub.tw/bbs3/memberlist.php?mode=viewprofile&amp;u=48">dora</a> — Mon Aug 14, 2017 10:27 pm</p><hr />
